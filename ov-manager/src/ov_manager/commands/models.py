def _humanize_bytes(n: int) -> str:
    """Return a human-friendly string for *n* bytes.

    Args:
        n: Number of bytes.

    Returns:
        Human-readable size string (e.g. ``1.2 GB``).
    """
    for unit in ("B", "KB", "MB", "GB", "TB"):
        if abs(n) < 1024:
            return f"{n:.1f} {unit}"
        n //= 1024
    return f"{n:.1f} PB"


def _dir_size(path: Path) -> int:
    """Return total size of all files under *path* in bytes.

    Args:
        path: Directory to measure.

    Returns:
        Total size in bytes, or 0 if path does not exist.
    """
    if not path.exists():
        return 0
    return sum(f.stat().st_size for f in path.rglob("*") if f.is_file())


def _load_config_json(config_json: Path) -> dict[str, Any]:
    """Load and parse the OVMS ``config.json``.

    Args:
        config_json: Path to the OVMS top-level config.json.

    Returns:
        Parsed dict, or empty dict if the file doesn't exist or is malformed.
    """
    if not config_json.exists():
        return {}
    try:
        return json.loads(config_json.read_text())  # type: ignore[no-any-return]
    except (json.JSONDecodeError, OSError):
        return {}


_CALCULATOR_TASK_MAP: dict[str, str] = {
    "HttpLLMCalculator": "text_generation",
    "ImageGenCalculator": "image_generation",
    "EmbeddingsCalculator": "embeddings",
    "EmbeddingsCalculatorOV": "embeddings",
    "RerankCalculator": "rerank",
    "RerankCalculatorOV": "rerank",
}

_RE_CALCULATOR = re.compile(r'calculator\s*:\s*"([^"]+)"')
_RE_DEVICE = re.compile(r'device\s*:\s*"([^"]+)"')


def _parse_graph_pbtxt(model_dir: Path) -> tuple[str, str]:
    """Parse ``graph.pbtxt`` to extract task type and device.

    Reads the graph.pbtxt file generated by OVMS ``--pull`` and infers the
    task from the calculator name, and reads the device from the node options.

    Args:
        model_dir: Directory containing the model files and ``graph.pbtxt``.

    Returns:
        Tuple of ``(task, device)``. Both are empty strings if the file is
        absent or the values cannot be determined.
    """
    graph_path = model_dir / "graph.pbtxt"
    if not graph_path.exists():
        return "", ""

    try:
        content = graph_path.read_text()
    except OSError:
        return "", ""

    task = ""
    for match in _RE_CALCULATOR.finditer(content):
        calculator = match.group(1)
        if calculator in _CALCULATOR_TASK_MAP:
            task = _CALCULATOR_TASK_MAP[calculator]
            break

    device_match = _RE_DEVICE.search(content)
    device = device_match.group(1) if device_match else ""

    return task, device


def _scan_unregistered(models_dir: Path, registered_names: set[str]) -> list[dict[str, Any]]:
    """Scan *models_dir* for downloaded-but-unregistered models.

    A directory is considered an OVMS model if it contains a ``graph.pbtxt``
    file. The model name is derived from its path relative to *models_dir*
    (using forward slashes to match HuggingFace org/model convention).

    Args:
        models_dir: Model repository root.
        registered_names: Set of model names already in config.json.

    Returns:
        List of entry dicts for unregistered models.
    """
    if not models_dir.exists():
        return []

    entries: list[dict[str, Any]] = []
    for graph in models_dir.rglob("graph.pbtxt"):
        model_dir = graph.parent
        rel = model_dir.relative_to(models_dir)
        # Use forward-slash convention to match HF model IDs
        name = rel.as_posix()
        if name in registered_names:
            continue
        task, device = _parse_graph_pbtxt(model_dir)
        size = _dir_size(model_dir)
        entries.append(
            {
                "name": name,
                "task": task,
                "device": device,
                "registered": False,
                "size": size,
            }
        )
    return entries


def _model_entries(config_json: Path, models_dir: Path) -> list[dict[str, Any]]:
    """Extract model entries from OVMS config.json and filesystem scan.

    Merges registered models (from config.json) with unregistered models
    discovered by scanning *models_dir* for ``graph.pbtxt`` files.

    Args:
        config_json: Path to the OVMS top-level config.json.
        models_dir: Model repository root.

    Returns:
        List of enriched model dicts with keys: name, path, task, device,
        registered, size.
    """
    data = _load_config_json(config_json)
    raw_models: list[dict[str, Any]] = data.get("model_config_list", [])

    entries: list[dict[str, Any]] = []
    registered_names: set[str] = set()

    for item in raw_models:
        cfg: dict[str, Any] = item.get("config", item)
        name: str = cfg.get("name", "")
        base_path: str = cfg.get("base_path", cfg.get("model_path", ""))
        registered_names.add(name)

        # Resolve the actual directory: base_path may be absolute or relative
        # to models_dir.
        model_dir = Path(base_path)
        if not model_dir.is_absolute():
            model_dir = models_dir / base_path

        task, device = _parse_graph_pbtxt(model_dir)
        size = _dir_size(model_dir)

        entries.append(
            {
                "name": name,
                "task": task,
                "device": device,
                "registered": True,
                "size": size,
            }
        )

    entries.extend(_scan_unregistered(models_dir, registered_names))
    return entries


