@models.command("get")
@click.argument("source_model")
@click.option(
    "--task",
    required=True,
    type=click.Choice(list(VALID_TASKS)),
    help="OVMS task type for this model.",
)
@click.option(
    "--device",
    default="CPU",
    show_default=True,
    type=click.Choice(list(VALID_DEVICES)),
    help="Target inference device.",
)
@click.option(
    "--name",
    default=None,
    help="Model name exposed by the server. Defaults to SOURCE_MODEL.",
)
@click.option(
    "--weight-format",
    default=None,
    help=(
        "Weight precision passed to optimum-cli for models that require conversion "
        "(e.g. int8, int4). Omit for pre-converted OpenVINO models (OpenVINO/ org)."
    ),
)
@click.option(
    "--overwrite",
    is_flag=True,
    default=False,
    help="Overwrite an existing model with the same name.",
)
@click.option(
    "--pipeline-type",
    default=None,
    type=click.Choice(list(VALID_PIPELINE_TYPES)),
    help="Pipeline sub-type (text_generation only).",
)
@click.option(
    "--cache-size",
    default=None,
    type=int,
    help="KV cache size in GB (text_generation only).",
)
@click.option(
    "--token",
    default=None,
    envvar=["OVMGR_HF_TOKEN", "HF_TOKEN"],
    help="HuggingFace token for gated models. Defaults to $OVMGR_HF_TOKEN or $HF_TOKEN.",
)
@click.pass_obj
def get_model(
    config: Config,
    source_model: str,
    task: str,
    device: str,
    name: str | None,
    weight_format: str,
    overwrite: bool,
    pipeline_type: str | None,
    cache_size: int | None,
    token: str | None,
) -> None:
    r"""Download and register a model from HuggingFace.

    SOURCE_MODEL is the HuggingFace model ID, e.g.:

    \b
        OpenVINO/whisper-base-fp16-ov
        OpenVINO/Qwen3-Embedding-0.6B-fp16-ov
        meta-llama/Llama-3.2-3B-Instruct  (requires --token for gated models)

    The model is first pulled into --models-dir, then registered in the OVMS
    config.json. Both steps are required; if registration fails the error is
    reported clearly.
    """
    resolved_name = name or source_model
    # Token: CLI flag wins; otherwise fall back to config-resolved token.
    resolved_token = token or config.hf_token

    config.models_dir.mkdir(parents=True, exist_ok=True)

    backend = get_backend(config)

    pull_params = PullParams(
        source_model=source_model,
        model_repository_path=config.models_dir,
        task=task,
        model_name=resolved_name,
        target_device=device,
        weight_format=weight_format,
        overwrite=overwrite,
        pipeline_type=pipeline_type,
        cache_size=cache_size,
        hf_token=resolved_token,
    )

    register_params = RegisterParams(
        config_json_path=config.models_dir / "config.json",
        model_repository_path=config.models_dir,
        model_name=resolved_name,
        model_path=resolved_name,
    )

    # Step 1: Pull
    console.rule(f"[bold]Pulling[/bold] {source_model}")
    try:
        backend.pull(pull_params)
    except click.ClickException:
        raise
    except Exception as exc:
        console.print(
            Panel(
                f"[bold red]Pull failed:[/bold red] {exc}",
                title="Error",
                border_style="red",
            )
        )
        raise click.Abort() from exc

    # Step 2: Register — check current state first to avoid OVMS duplicate-name error
    config_json = config.models_dir / "config.json"
    already_registered = any(
        e["name"] == resolved_name and e["registered"]
        for e in _model_entries(config_json, config.models_dir)
    )

    if already_registered and not overwrite:
        console.print(
            "[yellow]Already registered in config.json — skipping registration.[/yellow]\n"
            "Use [bold]--overwrite[/bold] to force re-registration."
        )
    else:
        if already_registered and overwrite:
            # Remove existing registration before re-registering
            console.rule(f"[bold]Re-registering[/bold] {resolved_name}")
            backend.remove(RemoveParams(config_json_path=config_json, model_name=resolved_name))
        else:
            console.rule(f"[bold]Registering[/bold] {resolved_name}")

        try:
            backend.register(register_params)
        except click.ClickException:
            console.print(
                Panel(
                    "[bold red]Registration failed.[/bold red] "
                    f"The model files were downloaded to [cyan]{config.models_dir}[/cyan] "
                    "but could not be registered in config.json.\n\n"
                    "Retry registration with:\n"
                    f"  [bold]ov-manage models register '{resolved_name}'[/bold]",
                    title="Registration Error",
                    border_style="red",
                )
            )
            raise
        except Exception as exc:
            console.print(
                Panel(
                    f"[bold red]Registration failed:[/bold red] {exc}\n\n"
                    f"Model files are at [cyan]{config.models_dir}[/cyan].",
                    title="Registration Error",
                    border_style="red",
                )
            )
            raise click.Abort() from exc

    console.print(
        Panel(
            f"[bold green]Done![/bold green] "
            f"[cyan]{resolved_name}[/cyan] is ready.\n"
            f"Task: [green]{task}[/green]  Device: [yellow]{device}[/yellow]  "
            f"Models dir: [dim]{config.models_dir}[/dim]",
            title="Model Ready",
            border_style="green",
        )
    )


