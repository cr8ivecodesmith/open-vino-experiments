@models.command("list")
@click.option("--json", "as_json", is_flag=True, default=False, help="Output as JSON.")
@click.pass_obj
def list_models(config: Config, as_json: bool) -> None:
    """List all registered models in the model repository.

    Reads the OVMS config.json from --models-dir and displays each registered
    model with its task, device, and size on disk.
    """
    entries = _model_entries(config.config_json_path, config.models_dir)

    if as_json:
        output = [
            {k: v for k, v in e.items() if k != "size"}
            | {"size_bytes": e["size"], "size_human": _humanize_bytes(e["size"])}
            for e in entries
        ]
        click.echo(json.dumps(output, indent=2))
        return

    if not entries:
        table = Table(title="Models", show_lines=True)
        for col in ("Name", "Task", "Device", "Registered", "Size on Disk"):
            table.add_column(col)
        console.print(table)
        console.print(
            "[dim]No models found. Run [bold]ov-manage models get[/bold] to add one.[/dim]"
        )
        return

    table = Table(title="Models", show_lines=True)
    table.add_column("Name", style="bold cyan")
    table.add_column("Task", style="green")
    table.add_column("Device", style="yellow")
    table.add_column("Registered", justify="center")
    table.add_column("Size on Disk", justify="right")

    for e in entries:
        reg_cell = "[green]Y[/green]" if e["registered"] else "[red]N[/red]"
        table.add_row(
            e["name"],
            e["task"] or "—",
            e["device"] or "—",
            reg_cell,
            _humanize_bytes(e["size"]),
        )

    console.print(table)


