def _unregister_model(config: Config, config_json: Path, name: str) -> None:
    """Unregister *name* from config.json via the backend, with error formatting."""
    backend = get_backend(config)
    remove_params = RemoveParams(
        config_json_path=config_json,
        model_name=name,
    )
    console.rule(f"[bold]Unregistering[/bold] {name}")
    try:
        backend.remove(remove_params)
    except click.ClickException:
        console.print(
            Panel(
                f"[bold red]Unregister failed.[/bold red] "
                f"Could not remove [cyan]{name}[/cyan] from config.json.",
                title="Error",
                border_style="red",
            )
        )
        raise
    except Exception as exc:
        console.print(
            Panel(
                f"[bold red]Unregister failed:[/bold red] {exc}",
                title="Error",
                border_style="red",
            )
        )
        raise click.Abort() from exc


@models.command("rm")
@click.argument("name")
@click.option(
    "--purge",
    is_flag=True,
    default=False,
    help="Also delete the model files from disk.",
)
@click.option(
    "-y",
    "--yes",
    is_flag=True,
    default=False,
    help="Skip confirmation prompt (for scripting).",
)
@click.pass_obj
def remove_model(config: Config, name: str, purge: bool, yes: bool) -> None:
    """Remove a model from the OVMS registry and optionally from disk.

    NAME is the model name as shown in ``models list``
    (e.g. OpenVINO/whisper-base-fp16-ov).

    By default, only unregisters the model from config.json. Use --purge to
    also delete the model files from disk.
    """
    config_json = config.models_dir / "config.json"
    model_dir = config.models_dir / name

    # Determine current state
    entries = _model_entries(config_json, config.models_dir)
    match = next((e for e in entries if e["name"] == name), None)

    if match is None:
        raise click.ClickException(
            f"Model '{name}' not found in registry or on disk under {config.models_dir}."
        )

    is_registered = match["registered"]
    has_files = model_dir.exists()

    if not is_registered and not purge:
        raise click.ClickException(
            f"Model '{name}' is not registered in config.json and --purge was not given. "
            "Nothing to do."
        )

    # Confirmation for --purge
    if purge and has_files and not yes:
        click.confirm(
            f"WARNING: --purge will delete '{model_dir}' from disk. Continue?",
            abort=True,
        )

    # Step 1: Unregister
    if is_registered:
        _unregister_model(config, config_json, name)

    # Step 2: Delete files
    if purge:
        if has_files:
            console.rule(f"[bold]Deleting[/bold] {model_dir}")
            shutil.rmtree(model_dir)
            console.print(f"[dim]Deleted {model_dir}[/dim]")
        else:
            console.print(
                f"[yellow]Warning:[/yellow] --purge requested but "
                f"[cyan]{model_dir}[/cyan] was not found on disk."
            )

    parts = []
    if is_registered:
        parts.append("unregistered from config.json")
    if purge and has_files:
        parts.append("files deleted from disk")

    console.print(
        Panel(
            f"[bold green]Done![/bold green] [cyan]{name}[/cyan]: {', '.join(parts)}.",
            title="Removed",
            border_style="green",
        )
    )
