@models.command("register")
@click.argument("name")
@click.option(
    "--model-path",
    default=None,
    help="Path within the model repository. Defaults to NAME.",
)
@click.pass_obj
def register_model(config: Config, name: str, model_path: str | None) -> None:
    """Register an already-downloaded model in the OVMS config.json.

    NAME is the model name as it was downloaded (typically the HuggingFace
    model ID, e.g. OpenVINO/whisper-base-fp16-ov).

    Use this to retry a failed registration after a successful pull, or to
    manually register a model that was placed in the model repository by
    other means.
    """
    resolved_path = model_path or name

    backend = get_backend(config)

    register_params = RegisterParams(
        config_json_path=config.models_dir / "config.json",
        model_repository_path=config.models_dir,
        model_name=name,
        model_path=resolved_path,
    )

    console.rule(f"[bold]Registering[/bold] {name}")
    try:
        backend.register(register_params)
    except click.ClickException:
        console.print(
            Panel(
                f"[bold red]Registration failed.[/bold red] "
                f"Check that [cyan]{config.models_dir / resolved_path}[/cyan] exists "
                "and contains a valid downloaded model.",
                title="Registration Error",
                border_style="red",
            )
        )
        raise
    except Exception as exc:
        console.print(
            Panel(
                f"[bold red]Registration failed:[/bold red] {exc}",
                title="Registration Error",
                border_style="red",
            )
        )
        raise click.Abort() from exc

    console.print(
        Panel(
            f"[bold green]Registered![/bold green] "
            f"[cyan]{name}[/cyan] added to config.json.\n"
            f"Models dir: [dim]{config.models_dir}[/dim]",
            title="Done",
            border_style="green",
        )
    )


